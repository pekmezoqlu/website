function CounterItem({ item, started }: { item: typeof items[number]; started: boolean }) {
  const count = useCountUp(item.target ?? 0, 2000, started && item.target !== null);
  const display = item.target !== null ? count : (item as { display: string }).display;
  return (
    <div className="text-center">
      {item.icon}
      <p className="text-2xl font-extrabold">{display}{item.suffix}</p>
      <p className="text-red-100 text-xs mt-0.5">{item.label}</p>
    </div>
  );
}

export default function StatsBar() {
  const ref = useRef<HTMLElement>(null);
  const [started, setStarted] = useState(false);

  useEffect(() => {
    const el = ref.current;
    if (!el) return;
    const observer = new IntersectionObserver(
      ([entry]) => { if (entry.isIntersecting) { setStarted(true); observer.disconnect(); } },
      { threshold: 0.3 }
    );
    observer.observe(el);
    return () => observer.disconnect();
  }, []);

  return (
    <section ref={ref} className="bg-red-600 text-white">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-3">
        <div className="grid grid-cols-2 md:grid-cols-4 gap-6">
          {items.map((item) => (
            <CounterItem key={item.label} item={item} started={started} />
          ))}
        </div>
      </div>
    </section>
  );
}
